using Xunit;

namespace LT.TEST.BOOK.ICS7._06._14
{
    public class C01
    {
        [Fact]
        public void _01()
        {
            //----------------------------------------------------------------------------------------------------
            // 传给 output parameter 的变量，可以不用初始化，因为方法里面会保证对它进行赋值，
            //----------------------------------------------------------------------------------------------------
            int x;

            //----------------------------------------------------------------------------------------------------
            // output parameter 在调用方法时，也必须使用 out 标记，
            //----------------------------------------------------------------------------------------------------
            LIB.BOOK.ICS7._06._14.C01.Fn01(out x);

            Assert.Equal(9, x);
        }

        [Fact]
        public void _02()
        {
            //----------------------------------------------------------------------------------------------------
            // [C# 7.0]
            // 可以不用专门声明一个变量再传给 output parameter，
            // 而是在调用方法的同时，声明这个变量，
            // 此处的变量类型可以使用 var，
            //----------------------------------------------------------------------------------------------------
            LIB.BOOK.ICS7._06._14.C01.Fn01(out int x);
            Assert.Equal(9, x);
            LIB.BOOK.ICS7._06._14.C01.Fn01(out var y);
            Assert.Equal(9, y);
        }
    }
}
