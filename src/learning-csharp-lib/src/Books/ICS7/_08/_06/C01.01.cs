namespace Luotao.Lib.Books.ICS7._08._06
{
    /// <summary>
    ///     8.6.1 virtual and override methods
    /// </summary>
    public class C0101
    {
        //----------------------------------------------------------------------------------------------------
        // virtual 方法可以被子类 override
        //----------------------------------------------------------------------------------------------------
        public virtual int Fn01()
        {
            return 1;
        }
    }

    public class C0101A : C0101
    {
        //----------------------------------------------------------------------------------------------------
        // override 覆写父类中的 virtual 方法
        //----------------------------------------------------------------------------------------------------
        public override int Fn01()
        {
            return 2;
        }

        public int Fn02()
        {
            return base.Fn01();
        }
    }
}
