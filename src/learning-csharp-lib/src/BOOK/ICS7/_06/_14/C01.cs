namespace LT.LIB.BOOK.ICS7._06._14
{
    /// <summary>
    /// 6.14 output parameters
    /// </summary>
    public class C01
    {
        //----------------------------------------------------------------------------------------------------
        // 使用 out 声明一个 output parameter，output parameter 跟 reference parameter 一样，是一个变量别名，
        //----------------------------------------------------------------------------------------------------
        public static void Fn01(out int x)
        {
            //----------------------------------------------------------------------------------------------------
            // 方法在返回之前，必须对 output parameter 进行赋值，
            //----------------------------------------------------------------------------------------------------
            x = 8;

            //----------------------------------------------------------------------------------------------------
            // 必须对 output parameter 赋值之后，才能访问它
            //----------------------------------------------------------------------------------------------------
            x += 1;
        }
    }
}
